
public class NumberLogicDemo1 {

	public static void main(String[] args) {
		
		System.out.println(10/2); // If we use (/) symbol it gives Coefficient value.
		System.out.println(10%2); // If we use (%) symbol it gives remainder value.
		
		System.out.println(56789/10);  //In this case If we use(/) symbol it takes out the last number and gives remaining value.
		System.out.println(56789%10);  //In this case If we use(%) symbol it gives only the last number of the given value.
		// If we use extra zeros in below numbers it will take out or give numbers based on 0's we will gave.
		
		System.out.println(9/10);
		System.out.println(9%10);
		
		System.out.println((678*10)+9);   //if we want to add next consecutive number then ((given Number *10)+ Last Number) is the Logic.
		

	}

}
