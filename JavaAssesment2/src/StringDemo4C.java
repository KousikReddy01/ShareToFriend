
public class StringDemo4C {

	public static void main(String[] args) {
		
		String s1 = "       RBG Technologies     ";
		System.out.println(s1);
		s1 = s1.trim();
		System.out.println(s1);
		
		String s2 = "RBG TECHNOLOGIES";
		System.out.println(s2);
		s2 = s2.toLowerCase();
		System.out.println(s2);

	}

}
